package entitats;

public class Ticket {	
	private Object id;
	private String codi;
	private Object idFlori;
	
	public Object getId() {
		return id;
	}
	public void setId(Object id) {
		this.id = id;
	}
	public String getCodi() {
		return codi;
	}
	public void setCodi(String codi) {
		this.codi = codi;
	}	
	public Object getIdFlori() {
		return idFlori;
	}
	public void setIdFlori(Object idFlori) {
		this.idFlori = idFlori;
	}
}
